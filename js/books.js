function addToCard1() {
    const quantity = document.getElementById("sasia1")
    const soldOut = document.getElementById("soldText1")
    var finalValue = parseInt(quantity.innerHTML) - 1
    quantity.innerHTML = finalValue
    if (finalValue < 0) {
        alert("Nuk ka me libra ne stock, prisni deri sa te rimbushet stock-u")
        finalValue++
        quantity.innerHTML = finalValue
        soldOut.innerHTML = "SHITUR"
    }
    else if (finalValue >= 0) {
        alert("Libri eshte shtuar ne shporte")
    }
}
